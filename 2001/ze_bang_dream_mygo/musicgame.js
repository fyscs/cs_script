// 当前歌曲数据
let currentSongData = null;

// 初始化游戏
function InitializeGame(songName) {
    // 检查歌曲是否存在
    if (!songDatabase[songName]) {
        Instance.Msg(`歌曲 "${songName}" 不存在`);
        return;
    }
    
    // 设置当前歌曲
    currentSong = songName;
    currentSongData = songDatabase[songName];
    
    // 生成谱面
    GenerateChart(songName);
    
    // 重置游戏状态
    nextNoteIndex = 0;
    gameStartTime = Instance.GetGameTime();
    gameActive = true;
    
    // 清空延迟消息队列
    delayedMessages = [];
    
    // 设置延迟显示歌曲信息
    if (DEBUG_ENABLED) {
        delayedSongInfo = {
            triggerTime: gameStartTime + 2.0, // 2秒后触发
            songName: songName,
            noteCount: currentChart.length
        };
    }
    
    // 开始游戏循环
    Instance.SetNextThink(Instance.GetGameTime()); // 高频率更新以确保精确性
}

function GenerateChart(songName) {
    const songData = songDatabase[songName];
    currentChart = [];
    
    // 计算每个节拍的持续时间（秒）
    const beatDuration = 60.0 / songData.bpm;
    
    // 上一个音符的轨道和时间（用于难度半径和间隔检测）
    let lastLane = Math.floor(COLUMNS / 2) + 1; // 默认从中间轨道开始
    let lastHitTime = 0; // 上一个音符的时间
    
    // 滑块序列状态跟踪 - 改为使用对象来跟踪多个同时激活的滑块序列
    let activeSlideSequences = {}; // 使用对象来存储活跃的滑块序列，key为序列ID，value为轨道
    let slideSequenceCounter = 0; // 用于生成唯一序列ID
    
    // 计算对应难度下的时间间隔
    const minInterval = MIN_INTERVAL * DIFFICULTY_FACTORS[songData.difficult];
    const secInterval = SEC_INTERVAL * DIFFICULTY_FACTORS[songData.difficult];
    const thiInterval = THI_INTERVAL * DIFFICULTY_FACTORS[songData.difficult];
    
    // 处理谱面数据
    for (let i = 0; i < songData.notes.length; i++) {
        const note = songData.notes[i];
        
        let lane;
        let slideSequenceId = -1;
        
        // 将节拍位置转换为时间（秒）
        const hitTime = note.beat * beatDuration;
        
        // 计算与上一个音符的时间间隔
        const timeInterval = hitTime - lastHitTime;
        
        // 检查是否为滑块类型（3, 4, 5）
        const isSlideNote = note.type >= 3 && note.type <= 5;
        
        if (isSlideNote) {
            // 处理滑块音符
            if (note.type === 3) {
                // 类型3：滑块开始 - 创建新的滑块序列
                const sequenceId = slideSequenceCounter++;
                
                // 为滑块开始音符生成轨道
                if (songData.useRandomPositions) {
                    lane = generateLaneBasedOnInterval(timeInterval, lastLane, songData.radius, 
                                                       minInterval, secInterval, thiInterval, i > 0);
                } else {
                    lane = note.lane;
                }
                
                // 记录滑块序列的轨道
                activeSlideSequences[sequenceId] = lane;
                slideSequenceId = sequenceId;
                
                if (DEBUG_ENABLED) {
                    Instance.Msg(`创建滑块序列 ${sequenceId}，轨道: ${lane}`);
                }
            } else {
                // 类型4或5：滑块中间或结束
                // 查找对应的滑块序列
                let foundSequenceId = -1;
                
                // 首先检查谱面数据中是否明确指定了轨道匹配
                if (note.lane >= 0) {
                    // 如果谱面数据指定了轨道，尝试找到使用该轨道的活跃滑块序列
                    for (const [seqId, seqLane] of Object.entries(activeSlideSequences)) {
                        if (seqLane === note.lane) {
                            foundSequenceId = parseInt(seqId);
                            break;
                        }
                    }
                }
                
                // 如果没有找到匹配的序列，使用最近创建的序列
                if (foundSequenceId === -1) {
                    const sequenceIds = Object.keys(activeSlideSequences);
                    if (sequenceIds.length > 0) {
                        // 使用最近创建的序列（ID最大的）
                        foundSequenceId = Math.max(...sequenceIds.map(id => parseInt(id)));
                    }
                }
                
                if (foundSequenceId !== -1) {
                    // 使用对应序列的轨道
                    lane = activeSlideSequences[foundSequenceId];
                    slideSequenceId = foundSequenceId;
                    
                    if (DEBUG_ENABLED) {
                        Instance.Msg(`滑块序列 ${foundSequenceId} 继续，轨道: ${lane}`);
                    }
                    
                    // 如果是滑块结束（类型5），移除该序列
                    if (note.type === 5) {
                        delete activeSlideSequences[foundSequenceId];
                        if (DEBUG_ENABLED) {
                            Instance.Msg(`结束滑块序列 ${foundSequenceId}`);
                        }
                    }
                } else {
                    // 没有找到活跃的滑块序列，作为普通音符处理
                    if (songData.useRandomPositions) {
                        lane = generateLaneBasedOnInterval(timeInterval, lastLane, songData.radius, 
                                                           minInterval, secInterval, thiInterval, i > 0);
                    } else {
                        lane = note.lane;
                    }
                    
                    if (DEBUG_ENABLED) {
                        Instance.Msg(`警告: 类型${note.type}音符没有找到对应的滑块序列，使用轨道: ${lane}`);
                    }
                }
            }
        } else {
            // 非滑块音符（类型1或2）
            // 正常生成轨道
            if (songData.useRandomPositions) {
                lane = generateLaneBasedOnInterval(timeInterval, lastLane, songData.radius, 
                                                   minInterval, secInterval, thiInterval, i > 0);
            } else {
                lane = note.lane;
            }
        }
        
        // 添加到当前谱面
        currentChart.push({
            type: note.type,
            lane: lane,
            hitTime: hitTime,
            spawnTime: hitTime, // 生成时间等于判定时间
            beat: note.beat, // 保存节拍信息用于调试输出
            slideSequenceId: slideSequenceId // 标记滑块序列
        });
        
        // 更新上一个音符的轨道和时间
        lastLane = lane;
        lastHitTime = hitTime;
    }
    
    // 按时间排序谱面
    currentChart.sort((a, b) => a.hitTime - b.hitTime);
    
    // 后处理：验证滑块序列的完整性
    validateSlideSequences(currentChart);
}

/**
 * 根据时间间隔生成轨道
 */
function generateLaneBasedOnInterval(timeInterval, lastLane, radius, minInterval, secInterval, thiInterval, hasPreviousNote) {
    let lane;

    if (hasPreviousNote && timeInterval == 0){
        // 同时间下落的方块使用相邻轨道，避免相同轨道
        const adjacentLanes = [];
        for (let i = Math.max(1, lastLane - 1); i <= Math.min(COLUMNS, lastLane + 1); i++) {
            if (i !== lastLane) adjacentLanes.push(i);
        }
        lane = adjacentLanes[Math.floor(Math.random() * adjacentLanes.length)] || 
            (lastLane === 1 ? 2 : COLUMNS - 1); // 边界情况备用
    } else if (hasPreviousNote && timeInterval < minInterval) {
        // 间隔小于最小间隔，使用同一个轨道
        lane = lastLane;
    } else if (hasPreviousNote && timeInterval < secInterval) {
        // 间隔小于第二间隔，使用以上一个音符为中心的1半径范围内随机生成
        const minLane = Math.max(1, lastLane - 1);
        const maxLane = Math.min(COLUMNS, lastLane + 1);
        lane = getLaneWithReducedSameProbability(minLane, maxLane, lastLane);
    } else if (hasPreviousNote && timeInterval < thiInterval && radius > 1) {
        // 间隔小于第三间隔且半径大于1，使用以上一个音符为中心的2半径范围内随机生成
        const minLane = Math.max(1, lastLane - 2);
        const maxLane = Math.min(COLUMNS, lastLane + 2);
        lane = getLaneWithReducedSameProbability(minLane, maxLane, lastLane);
    } else {
        // 随机位置模式：在以上一个音符为中心的半径范围内随机生成
        const minLane = Math.max(1, lastLane - radius);
        const maxLane = Math.min(COLUMNS, lastLane + radius);
        lane = getLaneWithReducedSameProbability(minLane, maxLane, lastLane);
    }
    
    return lane;
}

/**
 * 验证滑块序列的完整性
 */
function validateSlideSequences(chart) {
    const sequences = {};
    
    // 收集所有滑块序列
    for (let i = 0; i < chart.length; i++) {
        const note = chart[i];
        if (note.slideSequenceId !== -1) {
            if (!sequences[note.slideSequenceId]) {
                sequences[note.slideSequenceId] = [];
            }
            sequences[note.slideSequenceId].push({
                index: i,
                type: note.type,
                lane: note.lane
            });
        }
    }
    
    // 验证每个滑块序列
    for (const sequenceId in sequences) {
        const sequence = sequences[sequenceId];
        
        // 检查序列是否以类型3开始
        if (sequence.length > 0 && sequence[0].type !== 3) {
            Instance.Msg(`滑块序列 ${sequenceId} 不是以类型3开始`);
        }
        
        // 检查序列中所有音符是否使用相同的轨道
        const firstLane = sequence[0].lane;
        for (let i = 1; i < sequence.length; i++) {
            if (sequence[i].lane !== firstLane) {
                Instance.Msg(`滑块序列 ${sequenceId} 中的音符轨道不一致: 索引 ${sequence[i].index}`);
                // 强制统一轨道
                chart[sequence[i].index].lane = firstLane;
            }
        }
        
        if (DEBUG_ENABLED) {
            Instance.Msg(`滑块序列 ${sequenceId}: 包含 ${sequence.length} 个音符, 轨道 ${firstLane}`);
        }
    }
}

function getLaneWithReducedSameProbability(minLane, maxLane, targetLane) {
    const rangeSize = maxLane - minLane + 1;
    
    // 如果范围内只有一个可选位置，直接返回
    if (rangeSize === 1) {
        return minLane;
    }
    
    // 生成1-100的随机数
    const randomValue = Math.floor(Math.random() * 100) + 1;
    
    // 只有5%的概率选择与上一个相同的lane（可根据需要调整这个概率）
    if (randomValue <= 5) {
        return targetLane;
    } else {
        // 95%的概率从其他位置中选择
        // 创建不包括targetLane的候选数组
        const candidates = [];
        for (let lane = minLane; lane <= maxLane; lane++) {
            if (lane !== targetLane) {
                candidates.push(lane);
            }
        }
        
        // 从候选数组中随机选择
        const randomIndex = Math.floor(Math.random() * candidates.length);
        return candidates[randomIndex];
    }
}

// 游戏主循环
function GameLoop() {
    if (!gameActive) {
        return 0; // 停止游戏循环
    }
    
    const currentTime = Instance.GetGameTime() - gameStartTime;
    const absoluteTime = Instance.GetGameTime();
    
    // 处理延迟消息
    if (DEBUG_ENABLED) {
        // 检查延迟歌曲信息
        if (delayedSongInfo && absoluteTime >= delayedSongInfo.triggerTime) {
            Instance.Msg(`开始播放歌曲: ${delayedSongInfo.songName} (BPM: ${currentSongData.bpm}, 难度半径: ${currentSongData.radius})`);
            Instance.Msg(`总音符数: ${delayedSongInfo.noteCount}`);
            delayedSongInfo = null;
        }
        
        // 检查延迟砖块消息
        for (let i = delayedMessages.length - 1; i >= 0; i--) {
            if (absoluteTime >= delayedMessages[i].triggerTime) {
                Instance.Msg(delayedMessages[i].message);
                delayedMessages.splice(i, 1);
            }
        }
    }
    
    // 生成应该出现的音符
    while (nextNoteIndex < currentChart.length && 
           currentChart[nextNoteIndex].spawnTime <= currentTime) {
        SpawnNote(currentChart[nextNoteIndex]);
        nextNoteIndex++;
    }
    
    // 检查游戏是否结束（所有音符都已生成并超过谱面时间）
    if (nextNoteIndex >= currentChart.length && 
        currentTime > currentChart[currentChart.length - 1].hitTime + 5) {
        EndGame();
        return 0;
    }
    
    Instance.SetNextThink(Instance.GetGameTime()); // 继续游戏循环
    return;
}

// 生成单个音符 - 使用 EntFireAtName
function SpawnNote(note) {
    // 构建生成器实体名称
    const makerName = `music_game_brick${note.type}_maker${note.lane}`;
    
    // 只有在调试模式下才显示砖块生成信息
    if (DEBUG_ENABLED) {
        // 格式化节拍显示
        const formattedBeat = note.beat;
        
        // 添加到延迟消息队列，2秒后显示
        delayedMessages.push({
            triggerTime: Instance.GetGameTime() + 2.0,
            message: `生成砖块: 类型${note.type}, 轨道${note.lane}, 节拍${formattedBeat}`
        });
    }
    
    // 使用 EntFireAtName 触发生成器
    Instance.EntFireAtName({ name: makerName, input: "ForceSpawn" });
}

// 结束游戏
function EndGame() {
    gameActive = false;
    if (DEBUG_ENABLED) {
        Instance.Msg(`歌曲 "${currentSong}" 播放完毕`);
    }
}

// 停止游戏（用于回合结束时调用）
function StopGame() {
    gameActive = false;
    if (currentSong && DEBUG_ENABLED) {
        Instance.Msg(`回合结束，停止播放歌曲: ${currentSong}`);
    }
}

