// Called on each new round
Instance.OnScriptInput("SetModels", () => {
    let playerArray = Instance.FindEntitiesByClass("player");
    // Loop through players, if their STEAMID matches, give them the model they have been longing for
    for (const currentPlayer of playerArray) {
        if (currentPlayer.IsValid() && currentPlayer.GetHealth() > 0) {
            for (const winnerID of winner_arr) {
                if (currentPlayer.steamID == winnerID) {
                    Instance.Msg("AESTHETIC Contest Winner Found!");
                    currentPlayer.SetModel("characters/models/thefaces.vmdl");
                    // Also randomize the skin/bodygroup
                    Instance.EntFireAtTarget(currentPlayer, "SetMaterialGroup", Math.floor(Math.random() * 24).toString(), 0.05);
                    Instance.EntFireAtTarget(currentPlayer, "SetBodyGroup", "body," + Math.floor(Math.random() * 13).toString(), 0.05);
                }
            }
        }
    }
});
