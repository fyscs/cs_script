function quiz(slot) {
    const quiz_title = Instance.FindEntityByName("quiz" + slot);
    const quiz_answer1 = Instance.FindEntityByName("answer1_quiz" + slot);
    const quiz_answer2 = Instance.FindEntityByName("answer2_quiz" + slot);
    const wrong_tele_left = Instance.FindEntityByName("wrong_tele_left" + slot);
    const wrong_wall_left = Instance.FindEntityByName("left_breakable" + slot);
    const wrong_tele_right = Instance.FindEntityByName("wrong_tele_right" + slot);
    const wrong_wall_right = Instance.FindEntityByName("right_breakable" + slot);

    if (!quiz_title || !quiz_answer1 || !quiz_answer2 || !wrong_tele_left || !wrong_wall_left || !wrong_tele_right || !wrong_wall_right) {
        Instance.Msg("fail to find quiz entities");
    }

    Instance.EntFireAtTarget({ target: quiz_title, input: "SetMessage", value: quizs[slots[slot - 1]].quiz });

    const leftGroup = { answer: quiz_answer1, tele: wrong_tele_left, wall: wrong_wall_left };
    const rightGroup = { answer: quiz_answer2, tele: wrong_tele_right, wall: wrong_wall_right };

    const trueIsLeft = Math.random() < 0.5;
    const trueGroup = trueIsLeft ? leftGroup : rightGroup;
    const falseGroup = trueIsLeft ? rightGroup : leftGroup;

    Instance.EntFireAtTarget({ target: trueGroup.answer, input: "SetMessage", value: quizs[slots[slot - 1]].true });
    Instance.EntFireAtTarget({ target: trueGroup.tele, input: "kill" });
    Instance.EntFireAtTarget({ target: trueGroup.wall, input: "break" });
    Instance.EntFireAtTarget({ target: falseGroup.answer, input: "SetMessage", value: quizs[slots[slot - 1]].false });
}

