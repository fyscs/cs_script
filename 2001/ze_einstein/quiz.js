function randomPick(num) {
    const pool = Array.from({ length: quizs.length }, (_, i) => i);
    const res = [];
    for (let i = 0; i < num; i++) {
        const j = Math.floor(Math.random() * pool.length);
        res.push(pool[j]);
        pool.splice(j, 1);
    }
    return res;
}

